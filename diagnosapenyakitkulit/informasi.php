<div class="title">Informasi Sistem Pakar </div>
<br />
<b>Sistem pakar</b> (<i>expert system</i>) adalah program AI dengan basis pengetahuan (knowledge base) yang diperoleh dari pengalaman atau pengetahuan pakar atau ahli dan didukung mesin interensi/inferensi engine yang melakukan penalaran atau pelacakan terhadap sesuatu atau fakta-fakta dan aturan kaidah yang ada di basis pengetahuan setelah dilakukan pencarian, sehingga dicapai kesimpulan.
<br>
<br />
<b>Sistem Pakar Diagnosa Penyakit Kulit </b> adalah sarana untuk  konsultasi mengenai penyakit pada kulit, karena penyakit kulit memiliki beberapa gejala yang berbeda dengan jenis penyakit yang berbeda pula sehingga dapat memudahkan siapapun yang menggunakan aplikasi ini untuk mengetahui penyakit yang di deritanya.<br />
Sumber basis pengetahuan dari sistem ini diperoleh dari hasil study pustaka dari berbagai sumber yang berkaitan dengan Basis Pengetahuan Penyakit kulit.<br />
